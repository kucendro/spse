public class App {

    public static void main(String[] args) {
        // Ukázka cyklu "for"
        System.out.println("Cyklus 'for':");
        for (int promenna = 1; promenna <= 5; promenna++) {
            System.out.println("Hodnota Proměnné: " + promenna);
        }

        // Ukázka cyklu "while"
        System.out.println("\nCyklus 'while':");
        int promenna2 = 1;
        while (promenna2 <= 5) {
            System.out.println("Hodnota Proměnné 2: " + promenna2);
            promenna2++;
        }
    }
}
